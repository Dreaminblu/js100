// Write a function that checks whether a string is empty or not. For example:

function isBlank(str) {
  return str.length === 0;
}

isBlank('mars'); // false
isBlank('  ');   // false
isBlank('');     // true

console.log(isBlank('mars'));
console.log(isBlank('  '));
console.log(isBlank(''));




